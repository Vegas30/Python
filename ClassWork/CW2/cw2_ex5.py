# Пользователь вводит число и степень с клавиатуры. Программа
# вычисляет и выводит на экран результат возведения числа в
# указанную степень.

# Считать число и степень
base = float(input("Введите число: "))
exponent = float(input("Введите степень: "))

# Вычислить результат возведения числа в степень
result = base ** exponent

# Вывести результат
print(f"Результат возведения числа {base} в степень {exponent} равен {result}")
