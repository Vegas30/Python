# Даны два целых числа a, b. Найдите сумму, разность,
# произведение и частное их квадратов. Все значения вывести на
# одной строке, разделив пробелами.

# Считать два целых числа
a = int(input("Введите целое число a: "))
b = int(input("Введите целое число b: "))

# Вычислить сумму квадратов
sum_of_squares = a**2 + b**2

# Вычислить разность квадратов
difference_of_squares = a**2 - b**2

# Вычислить произведение квадратов
product_of_squares = a**2 * b**2

# Вычислить частное квадратов, проверив, что b не равно нулю
if b != 0:
    quotient_of_squares = a**2 / b**2
else:
    quotient_of_squares = "деление на ноль"

# Вывести результаты
print(f"Сумма: {sum_of_squares}, разность: {difference_of_squares}, произведение: {product_of_squares}, частное: {quotient_of_squares} квадратов a = {a} и b = {b}")
