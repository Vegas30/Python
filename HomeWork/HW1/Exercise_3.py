# –°–æ—Å—Ç–∞–≤–∏—Ç—å –ø—Ä–æ–≥—Ä–∞–º–º—É:
# –∞) –≤—ã—á–∏—Å–ª–µ–Ω–∏—è –∑–Ω–∞—á–µ–Ω–∏—è —Ñ—É–Ω–∫—Ü–∏–∏ ùë¶ = 7ùë•^2 ‚àí 3—Ö + 6 –ø—Ä–∏ –ª—é–±–æ–º
# –∑–Ω–∞—á–µ–Ω–∏–∏ x;
# –±) –≤—ã—á–∏—Å–ª–µ–Ω–∏—è –∑–Ω–∞—á–µ–Ω–∏—è —Ñ—É–Ω–∫—Ü–∏–∏ ùë• = 12–∞^2 + 7–∞ ‚àí 16 –ø—Ä–∏
# –ª—é–±–æ–º –∑–Ω–∞—á–µ–Ω–∏–∏ –∞.

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ –¥–ª—è x, –ø—Ä–∏–≤–æ–¥–∏–º –∫ –≤–µ—â–µ—Å—Ç–≤–µ–Ω–Ω—ã–º —á–∏—Å–ª–∞–º, –ø—Ä–∏—Å–≤–∞–∏–≤–∞–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π x
x = float(input("–ó–∞–¥–∞–Ω–∏–µ –∞) –í–≤–µ–¥–∏—Ç–µ –∑–Ω–∞—á–µ–Ω–∏–µ x: "))

# –í—ã—á–∏—Å–ª—è–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ —É –ø–æ —Ñ–æ—Ä–º—É–ª–µ: ùë¶ = 7ùë•^2 ‚àí 3—Ö + 6
y = 7 * x ** 2 - 3 * x + 6

# –í—ã–≤–æ–¥–∏–º –∑–Ω–∞—á–µ–Ω–∏–µ y –≤ —Ç–µ—Ä–º–∏–Ω–∞–ª
print(f"–ü—Ä–∏ x = {x}, –∑–Ω–∞—á–µ–Ω–∏–µ y = {y}")

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ –¥–ª—è a, –ø—Ä–∏–≤–æ–¥–∏–º –∫ –≤–µ—â–µ—Å—Ç–≤–µ–Ω–Ω—ã–º —á–∏—Å–ª–∞–º, –ø—Ä–∏—Å–≤–∞–∏–≤–∞–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π a
a = float(input("–ó–∞–¥–∞–Ω–∏–µ b) –í–≤–µ–¥–∏—Ç–µ –∑–Ω–∞—á–µ–Ω–∏–µ a: "))

# –í—ã—á–∏—Å–ª—è–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ —É –ø–æ —Ñ–æ—Ä–º—É–ª–µ: ùë• = 12–∞^2 + 7–∞ ‚àí 16
x = 12 * a ** 2 + 7 * a - 16

# –í—ã–≤–æ–¥–∏–º –∑–Ω–∞—á–µ–Ω–∏–µ x –≤ —Ç–µ—Ä–º–∏–Ω–∞–ª
print(f"–ü—Ä–∏ a = {a}, –∑–Ω–∞—á–µ–Ω–∏–µ x = {x}")

