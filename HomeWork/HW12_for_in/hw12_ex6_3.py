# Все задачи решить используя функции def. Для каждого пункта
# написать свою функцию.
# 6.3. Напечатать "столбиком":
# а) все целые числа от 20 до 35;
# б) квадраты всех целых чисел от 10 до b (значение b вводится с клавиатуры; b 10);
# в) третьи степени всех целых чисел от a до 50 (значение a вводится с клавиатуры; a 50);
# г) все целые числа от a до b (значения a и b вводятся с клавиатуры; b a).

# а) Все целые числа от 20 до 35
def print_integers(a, b):
    for i in range(a, b):
        print(i)


# б) Квадраты всех целых чисел от 10 до b
def print_squares_from_10_to_b(b):
    for i in range(10, b + 1):
        print(i ** 2)


# в) Третьи степени всех целых чисел от a до 50
def print_cubes_from_a_to_50(a):
    for i in range(a, 51):
        print(i ** 3)


# г) Все целые числа от a до b
def print_integers_from_a_to_b(a, b):
    for i in range(a, b + 1):
        print(i)


def main():
    # а)
    print("а) Все целые числа от 20 до 35:")
    print_integers(20, 35)

    # б)
    b = int(input("б) Введите целое число b (b > 10): "))
    if b > 10:
        print(f"Квадраты всех целых чисел от 10 до {b}:")
        print_squares_from_10_to_b(b)
    else:
        print("Ошибка: число b должно быть больше 10.")

    # в)
    a = int(input("в) Введите целое число a (a < 50): "))
    if a < 50:
        print(f"Третьи степени всех целых чисел от {a} до 50:")
        print_cubes_from_a_to_50(a)
    else:
        print("Ошибка: число a должно быть меньше 50.")

    # г)
    a = int(input("г) Введите целое число a: "))
    b = int(input("Введите целое число b (b >= a): "))
    if b >= a:
        print(f"Все целые числа от {a} до {b}:")
        print_integers_from_a_to_b(b, a)
    else:
        print("Ошибка: число b должно быть больше или равно a.")


if __name__ == "__main__":
    main()
