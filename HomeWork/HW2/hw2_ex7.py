# Запросите у пользователя цену товара и количество, которое он
# хочет купить. Посчитайте общую стоимость и выведите ее на экран.

# Запрашиваем у пользователя цену товара
price = float(input("Введите цену товара: "))

# Запрашиваем у пользователя количество товара
quantity = int(input("Введите количество товара, которое хотите купить: "))

# Рассчитываем общую стоимость
total_cost = price * quantity

# Выводим общую стоимость на экран
print(f"Общая стоимость: {total_cost}")
