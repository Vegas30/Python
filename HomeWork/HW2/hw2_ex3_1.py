# С начала суток прошло n секунд. Определить:
# а) сколько полных часов прошло с начала суток;
# б) сколько полных минут прошло с начала очередного часа;
# в) сколько полных секунд прошло с начала очередной минуты

# Вводим количество секунд
n = int(input("Введите количество секунд с начала суток: "))

# а) Находим количество полных часов
full_hours = n // 3600

# б) Находим количество полных минут с начала последнего часа
full_minutes = (n % 3600) // 60

# в) Находим количество полных секунд с начала последней минуты
full_seconds = n % 60

# Выводим результаты
print(f"Полных часов прошло с начала суток: {full_hours}")
print(f"Полных минут прошло с начала последнего часа: {full_minutes}")
print(f"Полных секунд прошло с начала последней минуты: {full_seconds}")
